package JavaMeanings;

public class Protected {
    //Protected er en access modifier, brugt til attributter, metoder og konstruktører, som gør dem tilgængelige for alle, hvis de er i samme (package) mappe eller sub klasse
    //Protected keyword bruges blandt andet når man extender fra en anden klasse.


    //Eksempel 1
  protected String fname = "John";
  protected String lname = "Doe";
  protected String email = "john@doe.com";
  protected int age = 24;
}
